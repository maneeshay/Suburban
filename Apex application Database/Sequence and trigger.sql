

DROP SEQUENCE seq_cart_id;

CREATE SEQUENCE seq_cart_id
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

CREATE OR REPLACE TRIGGER "CART_T1"
BEFORE INSERT
ON "CART"
FOR EACH ROW
BEGIN 
:new.cart_id:=seq_cart_id.nextval;
END;

/

